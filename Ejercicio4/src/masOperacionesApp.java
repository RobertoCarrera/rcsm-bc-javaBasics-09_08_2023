
public class masOperacionesApp {

	public static void main(String[] args) {
		
		int n = 5;
		
		System.out.println("El valor inicial de n = "+n); 
		n = n+77;
		System.out.println("N + 77 = "+(n));
		n = n-3;
		System.out.println("N - 3 = "+(n));
		n = n*2;
		System.out.println("N * 2 = "+(n));
	}

}
